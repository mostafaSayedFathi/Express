class Attendance
{
    private int ID;
    private int month;
    private int year;
    private int locationID;

    public void setID(int ID)
    {
        this.ID = ID;
    }
    public int getID()
    {
        return ID;
    }

    public void setMonth(int month)
    {
        this.month = month;
    }
    public int getMonth()
    {
        return month;
    }

    public void setYear(int year)
    {
        this.year = year;
    }
    public int getYear()
    {
        return year;
    }

    public void setLocationID(int locationID)
    {
        this.locationID = locationID;
    }
    public int getLocationID()
    {
        return locationID;
    }
}
